/**
 * Generic event.
 *
 * @package BLW\Core
 * @api BLW
 * @since 0.1.0
 * @author mAsT3RpEE <wotsyula@mast3rpee.tk>
 */
class Generic extends \BLW\Type\AEvent
{

    /**
     * Constructor
     *
     * @param mixed $Subject
     *            The subject of the event, usually an object.
     * @param array $Context
     *            Arguments to store in the event.
     */
    public function __construct($Subject = null, array $Context = array())
    {
        $this->_Subject = $Subject;
        $this->_Context = $Context;
    }
}

return true;
